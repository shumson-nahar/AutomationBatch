package java_basic_assignment;

public class While_loop1 {
	//WAP to print first 100 multiple of 5 and add those multiples 
			//5,10, 15 ...........500
			//5 + 10 + ................+500 = value
	
	public static void main(String[] args) {
		
		int i = 5;
		
		while(i<=500) {
			System.out.println(i);
			i=i+5;
		
	}
	}

}
